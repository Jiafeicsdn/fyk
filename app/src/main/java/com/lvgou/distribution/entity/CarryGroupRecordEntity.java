public class CarryGroupRecordEntity {

    private String id;
    private String title;
    private String grade;
    private String time;
    private String content;


    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getGrade() {
        return grade;
    }

    public void setGrade(String grade) {
        this.grade = grade;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public CarryGroupRecordEntity(String id, String title, String grade, String time, String content) {
        this.id = id;
        this.title = title;
        this.grade = grade;
        this.time = time;
        this.content = content;
    }

    @Override
    public String toString() {
        return "CarryGroupRecordEntity{" +
                "id='" + id + '\'' +
                ", title='" + title + '\'' +
                ", grade='" + grade + '\'' +
                ", time='" + time + '\'' +
                ", content='" + content + '\'' +
                '}';
    }
}
