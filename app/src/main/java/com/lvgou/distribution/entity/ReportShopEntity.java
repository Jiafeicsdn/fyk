public class ReportShopEntity {

    private String name;
    private String id;
    private String reportSellerID;

    public String getReportSellerID() {
        return reportSellerID;
    }

    public void setReportSellerID(String reportSellerID) {
        this.reportSellerID = reportSellerID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public ReportShopEntity(String name, String id, String reportSellerID) {
        this.name = name;
        this.id = id;
        this.reportSellerID = reportSellerID;
    }

    public ReportShopEntity() {
    }

    @Override
    public String toString() {
        return "ReportShopEntity{" +
                "name='" + name + '\'' +
                ", id='" + id + '\'' +
                '}';
    }
}
