public class ExchangeEntity {

    private String id;
    private String title;
    private String img_path;
    private String tuanbi;
    private String price;
    private String people;
    private String state;
    private String kucun;

    public String getKucun() {
        return kucun;
    }

    public void setKucun(String kucun) {
        this.kucun = kucun;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getImg_path() {
        return img_path;
    }

    public void setImg_path(String img_path) {
        this.img_path = img_path;
    }

    public String getTuanbi() {
        return tuanbi;
    }

    public void setTuanbi(String tuanbi) {
        this.tuanbi = tuanbi;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getPeople() {
        return people;
    }

    public void setPeople(String people) {
        this.people = people;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public ExchangeEntity(String id, String title, String img_path, String tuanbi, String price, String people, String state, String kucun) {
        this.id = id;
        this.title = title;
        this.img_path = img_path;
        this.tuanbi = tuanbi;
        this.price = price;
        this.people = people;
        this.state = state;
        this.kucun = kucun;
    }

    public ExchangeEntity() {
    }

    @Override
    public String toString() {
        return "ExchangeEntity{" +
                "id='" + id + '\'' +
                ", title='" + title + '\'' +
                ", img_path='" + img_path + '\'' +
                ", tuanbi='" + tuanbi + '\'' +
                ", price='" + price + '\'' +
                ", people='" + people + '\'' +
                ", state='" + state + '\'' +
                '}';
    }
}
