public class SearchResultEntity {

    private String id;
    private String name;
    private String adress;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAdress() {
        return adress;
    }

    public void setAdress(String adress) {
        this.adress = adress;
    }

    public SearchResultEntity(String id, String name, String adress) {
        this.id = id;
        this.name = name;
        this.adress = adress;
    }

    public SearchResultEntity() {
    }

    @Override
    public String toString() {
        return "SearchResultEntity{" +
                "id='" + id + '\'' +
                ", name='" + name + '\'' +
                ", adress='" + adress + '\'' +
                '}';
    }
}
