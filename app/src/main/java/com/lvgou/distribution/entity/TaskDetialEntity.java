public class TaskDetialEntity {
    private String id;
    private String name;
    private String status;
    private String tiem;
    private String potions;

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTiem() {
        return tiem;
    }

    public void setTiem(String tiem) {
        this.tiem = tiem;
    }

    public String getPotions() {
        return potions;
    }

    public void setPotions(String potions) {
        this.potions = potions;
    }

    public TaskDetialEntity(String id, String name, String tiem, String potions, String status) {
        this.id = id;
        this.name = name;
        this.tiem = tiem;
        this.potions = potions;
        this.status = status;
    }

    public TaskDetialEntity() {
    }

    @Override
    public String toString() {
        return "TaskDetialEntity{" +
                "id='" + id + '\'' +
                ", name='" + name + '\'' +
                ", tiem='" + tiem + '\'' +
                ", potions='" + potions + '\'' +
                '}';
    }
}
