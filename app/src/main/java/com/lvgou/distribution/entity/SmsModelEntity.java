public class SmsModelEntity {

    private String id;
    private String name;
    private String content;
    private String type;

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public SmsModelEntity(String id, String name, String content, String type) {
        this.id = id;
        this.name = name;
        this.content = content;
        this.type = type;
    }

    public SmsModelEntity() {
    }

    @Override
    public String toString() {
        return "SmsModelEntity{" +
                "id='" + id + '\'' +
                ", name='" + name + '\'' +
                ", content='" + content + '\'' +
                ", type='" + type + '\'' +
                '}';
    }
}
