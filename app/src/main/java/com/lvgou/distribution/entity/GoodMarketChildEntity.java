public class GoodMarketChildEntity {

    private String id;
    private String img_path;
    private String name;
    private String sale_price;
    private String commission;// 佣金
    private String sale_num;
    private String stock;// 库存
    private String quota;//大于0，显示分享

    public String getQuota() {
        return quota;
    }

    public void setQuota(String quota) {
        this.quota = quota;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getImg_path() {
        return img_path;
    }

    public void setImg_path(String img_path) {
        this.img_path = img_path;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSale_price() {
        return sale_price;
    }

    public void setSale_price(String sale_price) {
        this.sale_price = sale_price;
    }

    public String getCommission() {
        return commission;
    }

    public void setCommission(String commission) {
        this.commission = commission;
    }

    public String getSale_num() {
        return sale_num;
    }

    public void setSale_num(String sale_num) {
        this.sale_num = sale_num;
    }

    public String getStock() {
        return stock;
    }

    public void setStock(String stock) {
        this.stock = stock;
    }

    public GoodMarketChildEntity(String id, String stock, String commission, String sale_num, String sale_price, String name, String img_path, String quota) {
        this.id = id;
        this.stock = stock;
        this.commission = commission;
        this.sale_num = sale_num;
        this.sale_price = sale_price;
        this.name = name;
        this.img_path = img_path;
        this.quota = quota;
    }

    public GoodMarketChildEntity() {
    }


    @Override
    public String toString() {
        return "GoodMarketChildEntity{" +
                "id='" + id + '\'' +
                ", img_path='" + img_path + '\'' +
                ", name='" + name + '\'' +
                ", sale_price='" + sale_price + '\'' +
                ", commission='" + commission + '\'' +
                ", sale_num='" + sale_num + '\'' +
                ", stock='" + stock + '\'' +
                ", quota='" + quota + '\'' +
                '}';
    }
}
