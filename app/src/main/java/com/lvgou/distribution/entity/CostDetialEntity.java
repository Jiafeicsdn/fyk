public class CostDetialEntity {

    private String id;
    private String name;
    private String money;
    private String time;
    private String order_num;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getMoney() {
        return money;
    }

    public void setMoney(String money) {
        this.money = money;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getOrder_num() {
        return order_num;
    }

    public void setOrder_num(String order_num) {
        this.order_num = order_num;
    }

    public CostDetialEntity(String id, String name, String money, String time, String order_num) {
        this.id = id;
        this.name = name;
        this.money = money;
        this.time = time;
        this.order_num = order_num;
    }

    public CostDetialEntity() {
    }

    @Override
    public String toString() {
        return "CostDetialEntity{" +
                "id='" + id + '\'' +
                ", name='" + name + '\'' +
                ", money='" + money + '\'' +
                ", time='" + time + '\'' +
                ", order_num='" + order_num + '\'' +
                '}';
    }
}
