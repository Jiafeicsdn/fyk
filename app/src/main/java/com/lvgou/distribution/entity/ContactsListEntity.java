public class ContactsListEntity {

    private String id;
    private String name;
    private String img_path;
    private String fengwen_num;
    private String fangs_num;
    private String isFollow;
    private String sortLetters;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getFengwen_num() {
        return fengwen_num;
    }

    public void setFengwen_num(String fengwen_num) {
        this.fengwen_num = fengwen_num;
    }

    public String getImg_path() {
        return img_path;
    }

    public void setImg_path(String img_path) {
        this.img_path = img_path;
    }

    public String getFangs_num() {
        return fangs_num;
    }

    public void setFangs_num(String fangs_num) {
        this.fangs_num = fangs_num;
    }

    public String getIsFollow() {
        return isFollow;
    }

    public void setIsFollow(String isFollow) {
        this.isFollow = isFollow;
    }

    public String getSortLetters() {
        return sortLetters;
    }

    public void setSortLetters(String sortLetters) {
        this.sortLetters = sortLetters;
    }

    public ContactsListEntity(String id, String name, String img_path, String fengwen_num, String fangs_num, String isFollow, String sortLetters) {
        this.id = id;
        this.name = name;
        this.img_path = img_path;
        this.fengwen_num = fengwen_num;
        this.fangs_num = fangs_num;
        this.isFollow = isFollow;
        this.sortLetters = sortLetters;
    }

    public ContactsListEntity() {
    }

    @Override
    public String toString() {
        return "ContactsListEntity{" +
                "id='" + id + '\'' +
                ", name='" + name + '\'' +
                ", img_path='" + img_path + '\'' +
                ", fengwen_num='" + fengwen_num + '\'' +
                ", fangs_num='" + fangs_num + '\'' +
                ", isFollow='" + isFollow + '\'' +
                ", sortLetters='" + sortLetters + '\'' +
                '}';
    }
}
