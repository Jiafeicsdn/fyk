public class ReceiceRowTextEntity {

    private String id;// 消息 id
    private String snedId;// 消息发送者 id
    private String name;// 消息发送者 名字
    private String content;// 消息内容
    private String driect;// 发送与接收
    private String time;// 发送时间

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getDriect() {
        return driect;
    }

    public void setDriect(String driect) {
        this.driect = driect;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getSnedId() {
        return snedId;
    }

    public void setSnedId(String snedId) {
        this.snedId = snedId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public ReceiceRowTextEntity(String id, String snedId, String name, String content, String driect, String time) {
        this.id = id;
        this.snedId = snedId;
        this.name = name;
        this.content = content;
        this.driect = driect;
        this.time = time;
    }

    public ReceiceRowTextEntity() {
    }

    @Override
    public String toString() {
        return "ReceiceRowTextEntity{" +
                "id='" + id + '\'' +
                ", snedId='" + snedId + '\'' +
                ", name='" + name + '\'' +
                ", content='" + content + '\'' +
                '}';
    }
}
