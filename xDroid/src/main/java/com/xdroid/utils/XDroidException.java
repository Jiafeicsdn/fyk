public class XDroidException extends RuntimeException {

    private static final long serialVersionUID = -1L;

    public XDroidException(String detailMessage) {
        super(detailMessage);
    }

    public XDroidException(Throwable throwable) {
        super(throwable);
    }

    public XDroidException(String detailMessage, Throwable throwable) {
        super(detailMessage, throwable);
    }

}